const calculator = {
    add: function(a, b){
        console.log(a + b);
    },
    minus: function(a, b){
        console.log(a - b);
    },
    times: function(a, b){
        console.log(a * b);
    },
    divide: function(a, b){
        console.log(a / b);
    },
    powerOf: function(a, b){
        console.log(a ** b);
    }
}

calculator.add(5, 7)
calculator.minus(5, 7)
calculator.times(5, 7)
calculator.divide(5, 7)
calculator.powerOf(5, 7)